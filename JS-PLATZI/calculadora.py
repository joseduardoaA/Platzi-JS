o = input("Ingresa que operacion quieres realizar +, -, *, /")
num1 = int(input("Ingresa el primer numero"))
num2 = int(input("Ingresa el primer numero"))

if o == "+":
    resultado = num1 + num2
    print(resultado)
elif o == "-":
    resultado = num1 - num2
    print(resultado)
elif o == "*":
    resultado = num1 * num2
    print(resultado)
elif o == "/":
    resultado = num1 / num2
    print(resultado)


